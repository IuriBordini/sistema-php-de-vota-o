        <h2 class="text-center text-lg text-uppercase my-0">O que é
          <strong>Carro Tunado?</strong>
        </h2>
        <hr class="divider">
        <p>

O tuning é uma técnica usada no mundo inteiro que consiste na otimização, alteração e personalização extrema das características de um automóvel. Na maioria das vezes, costuma-se colocar traços da personalidade do dono, como alguma estampa, desenho ou formas que ele goste. O tuning também é conhecido por agregar valor desportivo aos carros, deixando-os mais potentes, resistentes e robustos fisicamente, com linhas e formas que aprimoram o movimento.

Praticamente todas as partes de um carro podem ser tunadas, desde uma roda até o sistema elétrico. Muitos chegam a gastar um valor que supera o preço do próprio veículo, com peças e acessórios como para-choques, saias, sistemas de óxido nitroso, entre muitos outros. Esses componentes extras, fazem com que um carro normal de série, torne-se totalmente exclusivo e com a cara do dono.

Ao contrário do que muita gente pensa, o tuning não altera somente a estética do carro, mas também adiciona mais potência ao motor, como mencionado anteriormente. Apesar disso, ele não deixa de lado o comportamento natural do carro nem a segurança, que são fatores indispensáveis na hora de dirigir.

Os apaixonados pelo tuning na maioria das vezes escolhem carros antigos ou populares para fazer as alterações e personalização. Isso devido ao preço baixo que esses veículos possuem e também por conta das formas vintage, que ficam bem legais após serem acoplados alguns artifícios.

Na maioria das vezes, quem utiliza o tuning, inspira-se nas competições do estilo Stock Car e Super Turismo Europeu, que contribuem bastante para a disseminação mundial da técnica. O cinema também influencia muito, como é o caso do filme “Velozes e Furiosos”, que exibe automóveis super tunados em corridas espetaculares, que matam qualquer um de inveja.</p>
